/** Minimal JPEG header bytes for file-type detection */
export const MINIMAL_JPEG_BYTES = Uint8Array.from([
  0xff, 0xd8, 0xff, 0xe0, 0x00, 0x10, 0x4a, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01,
  0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0xff, 0xd9,
]);

export function createTestFile(
  bytes: Uint8Array,
  name: string,
  type = "",
): File {
  return new File([bytes], name, { type, lastModified: 1_700_000_000_000 });
}

export function createPngFile(name = "photo.png"): File {
  return createTestFile(MINIMAL_PNG_BYTES, name, "text/plain");
}

export function createJpegFile(name = "photo.jpg"): File {
  return createTestFile(MINIMAL_JPEG_BYTES, name, "application/octet-stream");
}
